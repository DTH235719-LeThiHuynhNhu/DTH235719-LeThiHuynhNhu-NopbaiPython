# Ý nghĩa các toán tử trong Python

print("1) /  : Phép chia, luôn trả về số thực (float).")
print("2) // : Phép chia lấy phần nguyên (floor division), làm tròn xuống.")
print("3) %  : Phép chia lấy dư (modulo), dấu của kết quả theo số chia.")
print("4) ** : Lũy thừa (exponentiation).")
print("5) and: Toán tử logic AND, trả về toán hạng theo quy tắc short-circuit.")
print("6) or : Toán tử logic OR, trả về toán hạng theo quy tắc short-circuit.")
print("7) is : Toán tử kiểm tra định danh (identity), kiểm tra hai biến có cùng trỏ tới một object trong bộ nhớ hay không.")
