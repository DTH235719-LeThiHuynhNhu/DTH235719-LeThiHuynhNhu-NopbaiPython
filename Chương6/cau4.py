
# Khởi tạo dữ liệu
lst = [3, 0, 1, 5, 2]
x = 2

print("List =", lst)
print("x =", x)
print("---------------------------")

# Các câu hỏi
print("(a) lst[0] =", lst[0])          # phần tử đầu tiên
print("(b) lst[3] =", lst[3])          # phần tử thứ 4
print("(c) lst[x] =", lst[x])          # lst[2] = ?
print("(d) lst[-x] =", lst[-x])        # lst[-2] = phần tử thứ 2 từ cuối
print("(e) lst[x + 1] =", lst[x + 1])  # lst[3]
print("(f) lst[x] + 1 =", lst[x] + 1)  # giá trị tại lst[2] + 1
print("(g) lst[lst[x]] =", lst[lst[x]])  # lst[lst[2]] = lst[1]
print("(h) lst[lst[lst[x]]] =", lst[lst[lst[x]]])  # lst[lst[lst[2]]]
